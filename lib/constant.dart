Questo test di valutazione musicale  iniziale rivolto a ragazzi con disabilità intellettiva permette di investigare tre macro aree relative al rapporto che l'uomo ha sviluppato con i suoni e la musica, offre la possibilità di "fotografare":

1) l' Età musicale cioè l' apprendimento/euridizione musicale dell'assistito.
