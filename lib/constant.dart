3 = “Frequentemente” è la risposta idonea nel caso in cui la persona faccia o sia frequentemente soggetta a quanto specificato nell’item, ossia nella maggior parte dei casi. Ad esempio, per rispondere a "Mimica", si dovrà marcare l'opzione "frequentemente" se la persona in questione lo fa tra i 4 o i 6 volte durante l'incontro.

4 = “Sempre” è la miglior opzione nel caso in cui la persona faccia o sia sempre soggetta a quanto specificato nell’item. Ad esempio, per rispondere a "Mimica", si dovrà marcare l'opzione "sempre" se la persona in questione lo fa  in modo coerente e tutte le volte (anche nel caso in cui ci sia qualche eccezione).


