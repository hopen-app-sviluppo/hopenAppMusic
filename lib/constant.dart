Questa scheda ha la presunzione di affermare con certezza che ciò che “passa” da un punto di vista acustico rimane nella memoria e permette all'assistito con lieve, media e grave disabilità intellettiva  di sviluppare una serie di informazioni proto-comunicative significative che vengono impresse (forma, intensità e durata) negli eventi, diventando espressione di contenuti relazionali-affettivi.

