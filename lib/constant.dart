3) la "Musicofilia" cioè il modo in cui l'assistito  percepisce la musica, come la elaborata e utilizza all'interno del suo percorso storico evolutivo e nella complessità della sua vita.

Tutto parte dall’idea che gli assistiti con pluri disabilità, sindromici e con disabilità intellettiva anche gravissima sviluppano una capacità di elaborazione e utilizzo del suono molto più elaborata di quanto possiamo immaginare.
